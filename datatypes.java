/**
 * datatypes
 */
public class datatypes {

    public static void main(String[] args) {
        // byte a =127;
        int a = 12;
        float t = a;
        System.out.println(t);


        float b = 12.5f;

        int ine = (int)b;
        System.out.println("float to int:"+ine);


        boolean c = false;
        char ch = 'r';
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(ch);
        System.out.println("hello");

        // ASCII values


        int in = 43;
        char newin = (char)in;
        System.out.println(newin);

        char cr = 'A';
        int newcr = (int)cr;
        System.out.println(newcr);

        int $C_AR = 12;
        System.out.println($C_AR);
    }
}